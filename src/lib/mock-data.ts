export const verifyStudent = (
  rollNumber: string,
  name: string,
  section?: string
): { valid: boolean; student?: typeof mockStudentList[0] } => {
  const student = mockStudentList.find(
    (s) =>
      s.rollNumber.toLowerCase() === rollNumber.toLowerCase() &&
      s.name.toLowerCase() === name.toLowerCase() &&
      (section ? s.section === section : true)
  );
  
  return {
    valid: !!student,
    student,
  };
};

export const getStudentsBySection = (section: string) => {
  return mockStudentList.filter((s) => s.section === section);
};
